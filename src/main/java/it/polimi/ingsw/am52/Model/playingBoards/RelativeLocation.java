package it.polimi.ingsw.am52.Model.playingBoards;

/**
 * The location of a corner on the card face.
 */
public enum RelativeLocation {
    /**
     * At the top.
     */
    TOP,
    /**
     * At the bottom.
     */
    BOTTOM,
    /**
     * At the right.
     */
    RIGHT,
    /**
     * At the left.
     */
    LEFT,
    /**
     * At the top-right.
     */
    TOP_RIGHT,
    /**
     * At the bottom-right.
     */
    BOTTOM_RIGHT,
    /**
     * At the bottom-left.
     */
    BOTTOM_LEFT,
    /**
     * At the top-left.
     */
    TOP_LEFT
}
