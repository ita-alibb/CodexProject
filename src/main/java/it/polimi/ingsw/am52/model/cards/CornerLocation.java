/**
 * The location of a card corner on the card face.
 */
public enum CornerLocation {
    /**
     * At the top-right.
     */
    TOP_RIGHT,
    /**
     * At the bottom-right.
     */
    BOTTOM_RIGHT,
    /**
     * At the bottom-left.
     */
    BOTTOM_LEFT,
    /**
     * At the top-left.
     */
    TOP_LEFT
}
